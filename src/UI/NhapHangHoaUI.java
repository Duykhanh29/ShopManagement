    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // NhanVienBanHangUI nhanVienBanHangUI=new NhanVienBanHangUI(maNV);

        int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to confirm this bill? ");
        if (response == JOptionPane.YES_OPTION) {
            try {
                
                NhanVienBanHangUI nhanVienBanHangUI=new NhanVienBanHangUI(maNV);
             //   NhanVienBanHangUI nhanVienBanHangUI = new NhanVienBanHangUI();
                
                /*
                the job are:
                - push datas up to database
                after go back NhanVienBanHangUI
                - query the total costs and quatity of this bill
                */
                int totalQuantity=getTotalQuantityOfEachBill();
                int totalCosts=getTotalCostsOfEachBill();
                System.out.println("TotalQuantity: "+totalQuantity);
                System.out.println("TotalCosts: "+totalCosts);
                if(totalQuantity!=0){
                    HoaDon hd=new HoaDon(maHD, maNV, returnDate(), totalQuantity, totalCosts);
                    hoaDonJDBC.edit(maHD, hd);
                }else{
                   hoaDonJDBC.delete(maHD); 
                }
                nhanVienBanHangUI.setVisible(true);
                nhanVienBanHangUI.update(g);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(NhapHangHoaUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        
    }//GEN-LAST:event_confirmButtonActionPerformed

