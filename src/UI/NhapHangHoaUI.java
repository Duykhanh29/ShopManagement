    public void displayTK() {
        int sumQuantity = 0;
        int sumCosts = 0;
        for (int i = 0; i < temporaryList.size(); i++) {
            sumCosts = sumCosts + temporaryList.get(i).getTongTien();
            sumQuantity = sumQuantity + temporaryList.get(i).getSoLuong();
        }
        defaultTableModel.addRow(new Object[]{
            "", "", "", "", sumQuantity, sumCosts
        });
    }

    public void showTable(int index, ChiTietHoaDon chiTietHoaDon) {
        defaultTableModel.addRow(new Object[]{
            index, chiTietHoaDon.getMaHH(), chiTietHoaDon.getTenHangHoa(), chiTietHoaDon.getGiaBan(), chiTietHoaDon.getSoLuong(), chiTietHoaDon.getTongTien()
        });
    }

